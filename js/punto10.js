
var frase = prompt("Ingrese una frase: ");

frase = frase.toLowerCase();

for(i=0;i <= frase.length; i++){
    if(frase[i] == "a"){
        console.log("a")
    }
    if(frase[i] == "e"){
        console.log("e")
    }
    if(frase[i] == "i"){
        console.log("i")
    }
    if(frase[i] == "o"){
        console.log("o")
    }
    if(frase[i] == "u"){
        console.log("u")
    }
}
